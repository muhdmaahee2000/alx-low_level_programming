#include "3-calc.h"

/**
* op_add - computes addition of a by b
* @a: first number
* @b: second number
* Return: result of the addition of a by b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - computes subtraction of a by b
* @a: first number
* @b: second number
* Return: result of the subtraction of a by b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - computes multiplication of a by b
* @a: first number
* @b: second number
* Return: result of the multiplication of a by b
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - computes division of a by b
* @a: first number
* @b: second number
* Return: result of the division of a by b
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - computes remainder of the division of a by b
* @a: first number
* @b: second number
* Return: remainder of the division of a by b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
